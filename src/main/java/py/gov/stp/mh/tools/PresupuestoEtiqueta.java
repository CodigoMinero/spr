public class PresupuestoEtiqueta {
	
	protected int id;
	protected String productoConcat;
	protected int etiquetaId;												
	protected int productoId;
	protected int proyectoId;
	protected int subProgramaId;
	protected int programaId;
	protected int tipoPresupuestoId;
	protected int entidadId;
	protected int nivelId;
	protected int version;
	protected int anho;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getProductoConcat() {
		return productoConcat;
	}
	public void setProductoConcat(String productoConcat) {
		this.productoConcat = productoConcat;
	}
	public int getEtiquetaId() {
		return etiquetaId;
	}
	public void setEtiquetaId(int etiquetaId) {
		this.etiquetaId = etiquetaId;
	}
	public int getProductoId() {
		return productoId;
	}
	public void setProductoId(int productoId) {
		this.productoId = productoId;
	}
	public int getProyectoId() {
		return proyectoId;
	}
	public void setProyectoId(int proyectoId) {
		this.proyectoId = proyectoId;
	}
	public int getSubProgramaId() {
		return subProgramaId;
	}
	public void setSubProgramaId(int subProgramaId) {
		this.subProgramaId = subProgramaId;
	}
	public int getProgramaId() {
		return programaId;
	}
	public void setProgramaId(int programaId) {
		this.programaId = programaId;
	}
	public int getTipoPresupuestoId() {
		return tipoPresupuestoId;
	}
	public void setTipoPresupuestoId(int tipoPresupuestoId) {
		this.tipoPresupuestoId = tipoPresupuestoId;
	}
	public int getEntidadId() {
		return entidadId;
	}
	public void setEntidadId(int entidadId) {
		this.entidadId = entidadId;
	}
	public int getNivelId() {
		return nivelId;
	}
	public void setNivelId(int nivelId) {
		this.nivelId = nivelId;
	}
	public int getVersion() {
		return version;
	}
	public void setVersion(int version) {
		this.version = version;
	}
	public int getAnho() {
		return anho;
	}
	public void setAnho(int anho) {
		this.anho = anho;
	}
	
}

