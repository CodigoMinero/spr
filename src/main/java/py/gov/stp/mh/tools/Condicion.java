public class Condicion {
	protected int  nivel=0;
	protected int  entidad=0;
	protected int  tipo=0;
	protected int  programa=0;
	protected int  subprograma=0;
	protected int  proyecto=0;
	protected int  producto=0;
	protected int  snip=0;
	protected int  estrategia=0;
	public int getNivel() {
		return nivel;
	}
	public void setNivel(int nivel) {
		this.nivel = nivel;
	}
	public int getEntidad() {
		return entidad;
	}
	public void setEntidad(int entidad) {
		this.entidad = entidad;
	}
	public int getTipo() {
		return tipo;
	}
	public void setTipo(int tipo) {
		this.tipo = tipo;
	}
	public int getPrograma() {
		return programa;
	}
	public void setPrograma(int programa) {
		this.programa = programa;
	}
	public int getSubprograma() {
		return subprograma;
	}
	public void setSubprograma(int subprograma) {
		this.subprograma = subprograma;
	}
	public int getProyecto() {
		return proyecto;
	}
	public void setProyecto(int proyecto) {
		this.proyecto = proyecto;
	}
	public int getProducto() {
		return producto;
	}
	public void setProducto(int producto) {
		this.producto = producto;
	}
	public int getSnip() {
		return snip;
	}
	public void setSnip(int snip) {
		this.snip = snip;
	}
	public int getEstrategia() {
		return estrategia;
	}
	public void setEstrategia(int estrategia) {
		this.estrategia = estrategia;
	}

	
	
}
