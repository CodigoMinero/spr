public class Fundamentacion {
	protected int id;
	protected int estructura;
	protected int objetoGasto;
	protected int fuenteFinanciamiento;
	protected int organismoFinanciador;
	protected int pais;
	protected int depto;
	protected int secuencia;
	protected double precio;
	protected int cantidad;
	protected int cantidad_meses;
	protected String descripcion;
	protected int clgCodigo;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getEstructura() {
		return estructura;
	}
	public void setEstructura(int estructura) {
		this.estructura = estructura;
	}
	public int getObjetoGasto() {
		return objetoGasto;
	}
	public void setObjetoGasto(int objetoGasto) {
		this.objetoGasto = objetoGasto;
	}
	public int getFuenteFinanciamiento() {
		return fuenteFinanciamiento;
	}
	public void setFuenteFinanciamiento(int fuenteFinanciamiento) {
		this.fuenteFinanciamiento = fuenteFinanciamiento;
	}
	public int getOrganismoFinanciador() {
		return organismoFinanciador;
	}
	public void setOrganismoFinanciadro(int organismoFinanciador) {
		this.organismoFinanciador = organismoFinanciador;
	}
	public int getPais() {
		return pais;
	}
	public void setPais(int pais) {
		this.pais = pais;
	}
	public int getDepto() {
		return depto;
	}
	public void setDepto(int depto) {
		this.depto = depto;
	}
	public int getSecuencia() {
		return secuencia;
	}
	public void setSecuencia(int secuencia) {
		this.secuencia = secuencia;
	}
	public double getPrecio() {
		return precio;
	}
	public void setPrecio(double precio) {
		this.precio = precio;
	}
	public int getCantidad() {
		return cantidad;
	}
	public void setCantidad(int cantidad) {
		this.cantidad = cantidad;
	}
	public int getCantidad_meses() {
		return cantidad_meses;
	}
	public void setCantidad_meses(int cantidad_meses) {
		this.cantidad_meses = cantidad_meses;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public int getClgCodigo() {
		return clgCodigo;
	}
	public void setClgCodigo(int clgCodigo) {
		this.clgCodigo = clgCodigo;
	}
	public void setOrganismoFinanciador(int organismoFinanciador) {
		this.organismoFinanciador = organismoFinanciador;
	}

}
