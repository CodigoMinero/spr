public class UnidadJerarquica {

	protected int unJerId;
	protected String unJerNombre;
	protected String uniJerDescrip;
	protected int unJerEntidadId;
	protected int unJerEntidadNivelId;
	protected int unJerAnho;
	protected int unJeraNumeroFila;
	protected boolean unJeraBorrado;
	
	
	public int getUnJerId() {
		return unJerId;
	}
	public void setUnJerId(int unJerId) {
		this.unJerId = unJerId;
	}
	public String getUnJerNombre() {
		return unJerNombre;
	}
	public void setUnJerNombre(String unJerNombre) {
		this.unJerNombre = unJerNombre;
	}
	public String getUniJerDescrip() {
		return uniJerDescrip;
	}
	public void setUniJerDescrip(String uniJerDescrip) {
		this.uniJerDescrip = uniJerDescrip;
	}
	public int getUnJerEntidadId() {
		return unJerEntidadId;
	}
	public void setUnJerEntidadId(int unJerEntidadId) {
		this.unJerEntidadId = unJerEntidadId;
	}
	public int getUnJerEntidadNivelId() {
		return unJerEntidadNivelId;
	}
	public void setUnJerEntidadNivelId(int unJerEntidadNivelId) {
		this.unJerEntidadNivelId = unJerEntidadNivelId;
	}
	public int getUnJerAnho() {
		return unJerAnho;
	}
	public void setUnJerAnho(int unJerAnho) {
		this.unJerAnho = unJerAnho;
	}
	public int getUnJeraNumeroFila() {
		return unJeraNumeroFila;
	}
	public void setUnJeraNumeroFila(int unJeraNumeroFila) {
		this.unJeraNumeroFila = unJeraNumeroFila;
	}
	public boolean isUnJeraBorrado() {
		return unJeraBorrado;
	}
	public void setUnJeraBorrado(boolean unJeraBorrado) {
		this.unJeraBorrado = unJeraBorrado;
	}
	
}
