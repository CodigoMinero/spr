public class MatrizPnd {
	
	protected int lineaId;
	protected String lineaNombre;
	protected int ejeId;
	protected String ejeNombre;
	protected int estrategiaId;
	protected String estrategiaNombre;
	
	public int getLineaId() {
		return lineaId;
	}
	public void setLineaId(int lineaId) {
		this.lineaId = lineaId;
	}
	public String getLineaNombre() {
		return lineaNombre;
	}
	public void setLineaNombre(String lineaNombre) {
		this.lineaNombre = lineaNombre;
	}
	public int getEjeId() {
		return ejeId;
	}
	public void setEjeId(int ejeId) {
		this.ejeId = ejeId;
	}
	public String getEjeNombre() {
		return ejeNombre;
	}
	public void setEjeNombre(String ejeNombre) {
		this.ejeNombre = ejeNombre;
	}
	public int getEstrategiaId() {
		return estrategiaId;
	}
	public void setEstrategiaId(int estrategiaId) {
		this.estrategiaId = estrategiaId;
	}
	public String getEstrategiaNombre() {
		return estrategiaNombre;
	}
	public void setEstrategiaNombre(String estrategiaNombre) {
		this.estrategiaNombre = estrategiaNombre;
	}
	

}

