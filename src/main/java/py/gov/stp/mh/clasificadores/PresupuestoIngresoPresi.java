public class PresupuestoIngresoPresi {
	protected int numeroFila;
	protected int anio;
	protected int nivel;
	protected int entidad;
	protected int codOrigen;
	protected int codDetalle;
	protected int fuenteFinanc;
	protected double montoProgramado;
	protected int version;
	
	
	public int getNumeroFila() {
		return numeroFila;
	}
	public void setNumeroFila(int numeroFila) {
		this.numeroFila = numeroFila;
	}
	public int getAnio() {
		return anio;
	}
	public void setAnio(int anio) {
		this.anio = anio;
	}
	public int getNivel() {
		return nivel;
	}
	public void setNivel(int nivel) {
		this.nivel = nivel;
	}
	public int getEntidad() {
		return entidad;
	}
	public void setEntidad(int entidad) {
		this.entidad = entidad;
	}
	public int getCodOrigen() {
		return codOrigen;
	}
	public void setCodOrigen(int codOrigen) {
		this.codOrigen = codOrigen;
	}
	public int getCodDetalle() {
		return codDetalle;
	}
	public void setCodDetalle(int codDetalle) {
		this.codDetalle = codDetalle;
	}
	public int getFuenteFinanc() {
		return fuenteFinanc;
	}
	public void setFuenteFinanc(int fuenteFinanc) {
		this.fuenteFinanc = fuenteFinanc;
	}
	public double getMontoProgramado() {
		return montoProgramado;
	}
	public void setMontoProgramado(double montoProgramado) {
		this.montoProgramado = montoProgramado;
	}
	public int getVersion() {
		return version;
	}
	public void setVersion(int version) {
		this.version = version;
	}
	
	
	

}
