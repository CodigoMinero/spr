public class PresupuestoGastoPresi {
	
	protected int nivel;
	protected int entidad;
	protected int tipo;
	protected int programa;
	protected int subprograma;
	protected int proyecto;
	protected int objeto;
	protected int pais;
	protected int departamento;
	protected int fuente;
	protected int organismo;
	protected String verJustifica;
	protected double verProgramado;
	protected int fila;
	protected int anho;
	protected int version;
	
	
	
	public int getNivel() {
		return nivel;
	}
	public void setNivel(int nivel) {
		this.nivel = nivel;
	}
	public int getEntidad() {
		return entidad;
	}
	public void setEntidad(int entidad) {
		this.entidad = entidad;
	}
	public int getTipo() {
		return tipo;
	}
	public void setTipo(int tipo) {
		this.tipo = tipo;
	}
	public int getPrograma() {
		return programa;
	}
	public void setPrograma(int programa) {
		this.programa = programa;
	}
	public int getSubprograma() {
		return subprograma;
	}
	public void setSubprograma(int subprograma) {
		this.subprograma = subprograma;
	}
	public int getProyecto() {
		return proyecto;
	}
	public void setProyecto(int proyecto) {
		this.proyecto = proyecto;
	}
	public int getObjeto() {
		return objeto;
	}
	public void setObjeto(int objeto) {
		this.objeto = objeto;
	}
	public int getPais() {
		return pais;
	}
	public void setPais(int pais) {
		this.pais = pais;
	}
	public int getDepartamento() {
		return departamento;
	}
	public void setDepartamento(int departamento) {
		this.departamento = departamento;
	}
	public int getFuente() {
		return fuente;
	}
	public void setFuente(int fuente) {
		this.fuente = fuente;
	}
	public int getOrganismo() {
		return organismo;
	}
	public void setOrganismo(int organismo) {
		this.organismo = organismo;
	}
	public String getVerJustifica() {
		return verJustifica;
	}
	public void setVerJustifica(String verJustifica) {
		this.verJustifica = verJustifica;
	}
	public double getVerProgramado() {
		return verProgramado;
	}
	public void setVerProgramado(double verProgramado) {
		this.verProgramado = verProgramado;
	}
	public int getFila() {
		return fila;
	}
	public void setFila(int fila) {
		this.fila = fila;
	}
	public int getAnho() {
		return anho;
	}
	public void setAnho(int anho) {
		this.anho = anho;
	}
	public int getVersion() {
		return version;
	}
	public void setVersion(int version) {
		this.version = version;
	}


}
